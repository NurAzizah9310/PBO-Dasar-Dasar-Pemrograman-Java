public class No3_NurAzizahSantoso_Ganjil {
    public static void main(String[] args) {
        
        char c1 = '1';
        char c2 = '2';
        
        int ascii1 = (int) c1;
        int ascii2 = (int) c2;
        
        int jumlah = ascii1 + ascii2;
        
        System.out.println("ASCII dari karakter '1' = " + ascii1);
        System.out.println("ASCII dari karakter '2' = " + ascii2);
        System.out.println("Jumlah ASCII = " + jumlah);
    }
}
